package com.pojo;

public class Vc {
    int code;
    int VC;

    public Vc() {
    }

    public Vc(int code, int VC) {
        this.code = code;
        this.VC = VC;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public int getVC() {
        return VC;
    }

    public void setVC(int VC) {
        this.VC = VC;
    }
}
